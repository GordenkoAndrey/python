n = int(input("Enter n: "))                                                     #
i = 0                                                                           #
tmp = 2 * n - 1                                                                 #
output = 1                                                                      #
                                                                                #
while i <= tmp:                                                                 #
    if i % 2 != 0:                                                              #
        output *= i                                                             #
    i += 1                                                                      #
                                                                                #
print(output)                                                                   #
